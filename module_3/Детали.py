shop = [['каретка', 1200], ['шатун', 1000], ['седло', 300], ['педаль', 100],
['седло', 1500], ['рама', 12000], ['обод', 2000], ['шатун', 200], ['седло', 2700]]

name_detail = input('Название детали: ')
count = 0
price = 0
for item in shop:
    if item[0] == name_detail:
        count += 1
        price += item[1]

print('Количество деталей:', count)
print('Общая стоимость:', price)
